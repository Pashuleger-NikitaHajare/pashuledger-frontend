const tabs = [
  { key: 'home', path: '/', label: 'home' },
  { key: 'dairies', path: '/dairies', label: 'dairies' },
  { key: 'addVisit', path: '/visits/add', label: 'addVisit' },
  { key: 'farmers', path: '/farmers', label: 'farmers' },
  { key: 'report', path: '/report', label: 'report' },
  { key: 'settings', path: '/settings', label: 'settings' }
];

export default function BottomNav() {
  const navigate = useNavigate();
  const location = useLocation();
  const { t } = useTranslation();

  return (
    <nav className="bottom-nav">
      {tabs.map(tab => {
        const isActive = location.pathname === tab.path;
        const isAddVisit = tab.key === 'addVisit';
        return (
          <button
            key={tab.key}
            className={`nav-item${isActive ? ' active' : ''}${isAddVisit ? ' nav-add' : ''}`}
            onClick={() => navigate(tab.path)}
          >
            <span className={isAddVisit ? 'nav-add-icon' : ''}>{icons[tab.key]}</span>
            <span className="nav-label">{t(tab.label)}</span>
          </button>
        );
      })}
    </nav>
  );
}
